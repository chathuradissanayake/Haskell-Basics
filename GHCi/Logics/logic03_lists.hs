-- Lists Examples in Haskell

[1,2,3,4]          -- Explicit list
[1..8]             -- Range: [1,2,3,4,5,6,7,8]
['a','b','c','d']  -- Characters list (displays as "abcd")
['a'..'d']        -- Range of characters: "abcd"
[2,4..10]         -- Even numbers from 2 to 10: [2,4,6,8,10]
[1,3..10]         -- Odd numbers from 1 to 10: [1,3,5,7,9]