-- Generators - The Engine of Comprehensions

-- 1. Single Generator
[x * 2 | x <- [1,2,3,4]]
-- Result: [2,4,6,8]

-- 2. Multiple Generators

[(x,y) | x <- [1,2,3], y <- [4,5]]
-- Result: [(1,4),(1,5),(2,4),(2,5),(3,4),(3,5)]

[(x, y) | x <- [1,2], y <- ['a','b']]
-- Result: [(1,'a'),(1,'b'),(2,'a'),(2,'b')]



-- 3. order matters

[(x,y) | y <- [4,5], x <- [1,2,3]]
-- Result: [(1,4),(2,4),(3,4),(1,5),(2,5),(3,5)]

[(x,y) | x <- [1,2], y <- ['a','b']]
-- Result: [(1,'a'),(1,'b'),(2,'a'),(2,'b')]

[(x,y) | y <- ['a','b'], x <- [1,2]]
-- Result: [('a',1),('a',2),('b',1),('b',2)]


-- 4. Dependent Generators

[(x,y) | x <- [1..3], y <- [x..3]]
-- For x=1: y <- [1..3] → (1,1),(1,2),(1,3)
-- For x=2: y <- [2..3] → (2,2),(2,3)  
-- For x=3: y <- [3..3] → (3,3)
-- Result: [(1,1),(1,2),(1,3),(2,2),(2,3),(3,3)]

